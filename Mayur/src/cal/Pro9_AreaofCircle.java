package cal;

public class Pro9_AreaofCircle {

	public static void main(String[] args) 
	{
		float radius=5.2f;
		
		double parameter=2*Math.PI*radius;
		
		double area=Math.PI*radius*radius;
		
		
		System.out.println("Perimeter of Circle which having Radius 5.2 is = "+parameter);
		
		System.out.println("Area of Circle which having Radius 5.2 is = "+area);
		
	}

}
